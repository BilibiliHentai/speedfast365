
<h4>Windows懒人版客户端</h4>

<ol>

<li>
下载Speedfast365客户端<a href="/static/dl/Speedfast365-3.3.0.1.exe" target="_blank">点击这里</a>，右键管理员模式运行。(如果有管家卫士拦截，或者系统阻止，请放心，选择“允许运行”)
</li>
<li>
如果提示“需要安装.NET Framework”，则请<a target="_blank" href="/static/dl/net.framework.4.6.1.exe">点击此处</a>下载并安装.NET Framework后，再打开Speedfast365客户端。
</li>
<li>
Speedfast365客户端 会自动最小化至屏幕右下角通知区域(初始化可能需要1分钟，请耐心等待)。
</li>
<li>
出现登录框后，使用Speedfast365上注册的帐号和密码登录，程序又会自动最小化在右下角。
</li>
<li>
点击右下角图标，出现界面，如下图，点击“开启系统代理“，就好了。
<br />
	<img src="/static/image/divice_win_1.png" />
</li>
<li>
右键点击屏幕右下角图标--“更多配置和信息”，可以勾选“开机启动”，这样就更方便了。
</li>
<li>
然后，用IE浏览器打开https://www.google.com测试下是否能够正常访问。
</li>
<li>如果不能打开google、facebook等，请先换IE浏览器试试；别的哪个浏览器不行，就重新安装那个浏览器。</li>
<li>不建议使用360浏览器。推荐使用Chrome浏览器、firefox浏览器。</li>
</ol>


